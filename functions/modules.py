import math
import random
import datetime
print(math.sqrt(16)) 
# Output: 4.0
print(math.factorial(5))
print(math.pi)  # Output: 3.141592653589793

print(random.randint(1, 10))  # Output: Random integer between 1 and 10
print(random.choice(['apple', 'banana', 'cherry']))  # Output: Random choice
# from a list
print(random.random())  # Output: Random float between 0.0 and 1.

print(datetime.datetime.now())  # Output: Current date and time
print(datetime.date.today())  # Output: Current date    
